class UsersController < ApplicationController
  before_filter :authenticate_user!, :correct_user?, :except => :show

  # This page is public
  def show
    @user = User.find(params[:id])
  end

  def edit
    @user = User.find(params[:id])
  end

  def update
    @user = User.find(params[:id])
    if @user.update_attributes(params[:user])
      redirect_to @user
    else
      render :edit
    end
  end
end
